package com.personnel.pojo;

public class Department {
    private long id;//部门编号
    private String name;//部门名称

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
